        let stationItem = self.stationsList[indexPath.row]
        
        cell.address.text = stationItem.address
        cell.name.text = stationItem.name
        
        let distanceHardcoded = 2.5
        cell.distance.text = "\(distanceHardcoded) km"

        return cell
    }
    
